export function getAllCategories(): LibraryCategory[] {
  return LIBRARY_CATEGORIES
}

export function getCategoryById(id: string): LibraryCategory | undefined {
  return LIBRARY_CATEGORIES.find(c => c.id === id)
}

export function getAllBooks(): LibraryBook[] {
  return FEATURED_BOOKS
}

export function getFeaturedBooks(categoryId?: string): LibraryBook[] {
  if (categoryId) {
    return FEATURED_BOOKS.filter(b => b.categoryId === categoryId)
  }
  return FEATURED_BOOKS.filter(b => b.isFeatured)
}

export function getBooksByCategory(categoryId: string): LibraryBook[] {
  return FEATURED_BOOKS.filter(b => b.categoryId === categoryId)
}

export function getBookById(id: string): LibraryBook | undefined {
  return FEATURED_BOOKS.find(b => b.id === id)
}

export function getRelatedBooks(book: LibraryBook, limit = 3): LibraryBook[] {
  return FEATURED_BOOKS
    .filter(b => b.id !== book.id && b.categoryId === book.categoryId)
    .slice(0, limit)
}

export function getBooksByAuthor(authorAr: string, excludeId?: string): LibraryBook[] {
  return FEATURED_BOOKS.filter(
    b => b.authorAr === authorAr && b.id !== excludeId
  )
}

export function searchBooks(query: string): LibraryBook[] {
  if (!query.trim()) return []
  const q = query.toLowerCase().trim()
  return FEATURED_BOOKS.filter(b => {
    return (
      b.titleAr.includes(query) ||
      (b.titleFr?.toLowerCase().includes(q)) ||
      b.authorAr.includes(query) ||
      (b.authorFr?.toLowerCase().includes(q)) ||
      (b.description?.toLowerCase().includes(q))
    )
  })
}

export function getTotalBooksCount(): number {
  return LIBRARY_CATEGORIES.reduce((sum, c) => sum + c.bookCount, 0)
}

/**
 * URL Shamela pour un livre si on a son ID
 */
export function getShamela_url(shamelaId: number): string {
  return `https://shamela.ws/book/${shamelaId}`
}

/**
 * URL Archive.org pour un livre (recherche par titre)
 */
export function getArchiveUrl(titleAr: string): string {
  return `https://archive.org/search?query=${encodeURIComponent(titleAr)}`
}

/**
 * URL Islamhouse (recherche)
 */
export function getIslamhouseUrl(titleFr?: string, titleAr?: string): string {
  const q = titleFr ?? titleAr ?? ''
  return `https://islamhouse.com/fr/search/?q=${encodeURIComponent(q)}`
}
